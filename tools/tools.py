def add(x: float, y: float):
    """
    Inputs are x and y, Of float Type.
    Output will be the sum of x and y, (x + y)
    """
    return x + y

def subtract(x: int, y: int):
    """
    Inputs are x and y, Of float Type.
    Output will be the x - y.
    """
    return x - y


def divide(x: int, y: int):
    """
    Inputs are x and y, Of float Type.
    Output will be the x - y.
    """
    return x / y

def multiply(x: int, y: int):
    return x * y